// EnsureWirelessRecovery installs the boot-time USB rescue units and the
// capture-driver preload. Best-effort and idempotent; failures are never fatal.
func EnsureWirelessRecovery() {
	ensureRegDomain()
	ensureCaptureDriverPreload()
	ensureUSBColdBootRescue()
	ensureUSBWifiRecover()
}

// ensureRegDomain sets the regulatory domain so 5 GHz AP channels are usable;
// the world domain "00" marks every 5 GHz channel "no IR" and blocks hostapd.
func ensureRegDomain() {
	_ = ApplyRegDomain(os.Getenv("TALA_COUNTRY_CODE"))
}

// ApplyRegDomain sets the regulatory domain to the given two-letter country
// code (empty falls back to US), persisting it via modprobe.d and applying it
// immediately with `iw reg set`. Returns an error only if `iw reg set` fails.
func ApplyRegDomain(cc string) error {
	cc = strings.ToUpper(strings.TrimSpace(cc))
	if cc == "" {
		cc = "US"
	}
	if _, err := writeFileIdempotent("/etc/modprobe.d/tala-wte-regdom.conf",
		"options cfg80211 ieee80211_regdom="+cc+"\n", 0o644); err != nil {
		log.Printf("[usb] write regdom conf: %v", err)
	}
	iwBin := "iw"
	if _, err := exec.LookPath("iw"); err != nil {
		if _, statErr := os.Stat("/usr/sbin/iw"); statErr == nil {
			iwBin = "/usr/sbin/iw"
		}
	}
	if err := exec.Command(iwBin, "reg", "set", cc).Run(); err != nil {
		log.Printf("[usb] iw reg set %s: %v (persisted for next boot)", cc, err)
		return err
	}
	log.Printf("[usb] regulatory domain set to %s (enables 5 GHz AP)", cc)
	return nil
}

// ensureCaptureDriverPreload installs the modules-load.d snippet that preloads
// every supported USB Wi-Fi driver at boot.
func ensureCaptureDriverPreload() {
	if _, err := writeFileIdempotent(captureDriverPreloadPath, captureDriverPreload, 0o644); err != nil {
		log.Printf("[usb] write %s: %v", captureDriverPreloadPath, err)
		return
	}
	log.Println("[usb] modules-load: capture drivers pre-load at boot")
}

// ensureUSBColdBootRescue writes the xHCI rebind script + systemd oneshot and
// enables it. Idempotent across re-runs.
func ensureUSBColdBootRescue() {
	scriptChanged, err := writeFileIdempotent(usbRescueScriptPath, usbRescueScript, 0o755)
	if err != nil {
		log.Printf("[usb] write %s: %v", usbRescueScriptPath, err)
		return
	}
	unitChanged, err := writeFileIdempotent(usbRescueUnitPath, usbRescueUnit, 0o644)
	if err != nil {
		log.Printf("[usb] write %s: %v", usbRescueUnitPath, err)
		return
	}
	if scriptChanged || unitChanged {
		_ = exec.Command("systemctl", "daemon-reload").Run()
	}
	enableUnit("tala-wte-usb3-rescan.service")
	log.Println("[usb] systemd: tala-wte-usb3-rescan (xHCI cold-boot recovery) enabled")
}

// ensureUSBWifiRecover writes the per-device Wi-Fi firmware-wedge recovery
// script + systemd oneshot and enables it for every cold boot.
func ensureUSBWifiRecover() {
	scriptChanged, err := writeFileIdempotent(usbWifiRecoverScriptPath, usbWifiRecoverScript, 0o755)
	if err != nil {
		log.Printf("[usb] write %s: %v", usbWifiRecoverScriptPath, err)
		return
	}
	unitChanged, err := writeFileIdempotent(usbWifiRecoverUnitPath, usbWifiRecoverUnit, 0o644)
	if err != nil {
		log.Printf("[usb] write %s: %v", usbWifiRecoverUnitPath, err)
		return
	}
	if scriptChanged || unitChanged {
		_ = exec.Command("systemctl", "daemon-reload").Run()
	}
	enableUnit("tala-wte-wifi-recover.service")
	log.Println("[usb] systemd: tala-wte-wifi-recover (Wi-Fi firmware-wedge recovery) enabled for boot")
}

// HealWedgedWifiNow runs the per-device Wi-Fi wedge recovery once immediately
// instead of waiting for the next boot's systemd unit. Called by `tala-wte
// install`; kept out of the serve startup path because a permanently wedged
// adapter burns its full ~2 min retry budget and would delay every start.
func HealWedgedWifiNow() {
	if _, err := os.Stat(usbWifiRecoverScriptPath); err != nil {
		return
	}
	log.Println("[usb] scanning for wedged USB Wi-Fi adapters (up to ~90s per wedged device while it retries the firmware load)...")
	// Stream output live so the install does not appear frozen during retries.
	cmd := exec.Command(usbWifiRecoverScriptPath)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		log.Printf("[usb] tala-wte-wifi-recover run: %v", err)
	}
}

// enableUnit enables a systemd unit if it is not already enabled.
func enableUnit(unit string) {
	out, err := exec.Command("systemctl", "is-enabled", unit).Output()
	if err != nil || strings.TrimSpace(string(out)) != "enabled" {
		_ = exec.Command("systemctl", "enable", unit).Run()
	}
}

// writeFileIdempotent writes content with the given mode only when the existing
// content differs, returning whether the file was changed.
func writeFileIdempotent(path, content string, mode os.FileMode) (bool, error) {
	if existing, err := os.ReadFile(path); err == nil && string(existing) == content {
		return false, nil
	}
	if dir := dirOf(path); dir != "" {
		if err := os.MkdirAll(dir, 0o755); err != nil {
			return false, err
		}
	}
	if err := os.WriteFile(path, []byte(content), mode); err != nil {
		return false, err
	}
	// WriteFile honors mode only on create; enforce it for the executable bit.
	if err := os.Chmod(path, mode); err != nil {
		return false, err
	}
	return true, nil
}

// dirOf returns the directory portion of an absolute path.
func dirOf(path string) string {
	if i := strings.LastIndex(path, "/"); i > 0 {
		return path[:i]
	}
	return ""
}
