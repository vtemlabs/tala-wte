package deps

// USB wireless cold-boot recovery for physical capture/AP adapters. Two coupled
// failure modes: tala-wte-usb3-rescan rebinds xhci_hcd controllers for devices
// that never enumerate (xHCI SuperSpeed link-state miss), and
// tala-wte-wifi-recover does a per-device USB unbind/bind for adapters that
// enumerate but wedge on firmware load (MediaTek MT7921U "patch semaphore").
// tala-wte-capture.conf preloads the driver modules at boot.

import (
	"log"
	"os"
	"os/exec"
	"strings"
)

const (
	usbRescueScriptPath      = "/usr/local/sbin/tala-wte-usb3-rescan"
	usbRescueUnitPath        = "/etc/systemd/system/tala-wte-usb3-rescan.service"
	captureDriverPreloadPath = "/etc/modules-load.d/tala-wte-capture.conf"

	usbWifiRecoverScriptPath = "/usr/local/sbin/tala-wte-wifi-recover"
	usbWifiRecoverUnitPath   = "/etc/systemd/system/tala-wte-wifi-recover.service"
)

const usbRescueScript = `#!/bin/bash
# Tala WTE USB 3.0 cold-boot enumeration recovery.
#
# Some xHCI controllers fail to negotiate SuperSpeed link state for a USB 3.0
# device at cold boot. The device receives bus power but the kernel never logs
# a port-status-change interrupt, so it never enumerates. Unbinding then
# re-binding the xhci_hcd PCI driver tears down controller state and re-runs
# enumeration, picking up the device. Harmless on systems without the bug.
set -u

XHCI="/sys/bus/pci/drivers/xhci_hcd"
[ -d "$XHCI" ] || { echo "tala-wte-usb3-rescan: xhci_hcd driver dir not present" >&2; exit 0; }

shopt -s nullglob
for dev in "$XHCI"/0000:*; do
    addr=$(basename "$dev")
    echo "tala-wte-usb3-rescan: rebinding xHCI at $addr"
    echo "$addr" > "$XHCI/unbind" 2>/dev/null || echo "tala-wte-usb3-rescan: unbind $addr failed (continuing)" >&2
    sleep 2
    echo "$addr" > "$XHCI/bind"   2>/dev/null || echo "tala-wte-usb3-rescan: bind $addr failed"   >&2
    sleep 3
done
`

